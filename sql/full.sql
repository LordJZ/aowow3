CREATE TABLE `aowow_markup2_pages` (
  `id` mediumint(5) unsigned NOT NULL auto_increment,
  `url` varchar(40) NOT NULL,
  `title` varchar(100) NOT NULL default '',
  `icon` varchar(50) default '',
  `contents` mediumint(11) unsigned NOT NULL default '0',
  `quickfacts` mediumint(11) unsigned NOT NULL default '0',
  `view_access` tinyint(1) unsigned NOT NULL default '0',
  `edit_access` tinyint(1) unsigned NOT NULL default '0',
  `flags` tinyint(1) unsigned NOT NULL default '0',
  PRIMARY KEY  (`id`),
  UNIQUE KEY `url` (`url`)
) ENGINE=MyISAM DEFAULT CHARSET=utf8;

